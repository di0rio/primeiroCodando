#valor inicial ou sobrescrever uma

#atribuição simples | =
saldo = 2
print(saldo)

#com adição
saldo += 3
print(saldo)

#com subtração
saldo -= 1
print(saldo)

#com divisão
saldo //= 3
print(saldo)

saldo /= 1
print(saldo)

#com multiplicação
saldo *= 5
print(saldo)

#com exponenciação 
saldo **= 2
print(saldo)


#com modulo
saldo %= 2
print(saldo)
